package KuisPBO;

public class Pendaftaran {
    String nama;
    double nTulis, nCoding, nWawancara;

    public Pendaftaran(String nama, double nTulis, double nCoding, double nWawancara) {
        this.nama = nama;
        this.nTulis = nTulis;
        this.nCoding = nCoding;
        this.nWawancara = nWawancara;
    }
}
